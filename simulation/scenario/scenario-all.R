## SAVE AS "set-0.5b-all.RData"
##
##******************************************************************************


set.all <- lapply(c(25, 50, 200), function(S) {
  
  S   <- rep(S, 18)
  se  <- c(rep(0.5, 6), rep(0.8, 6), rep(0.9, 6))
  sp  <- c(rep(0.85,6), rep(0.8, 6), rep(0.4, 6))
  u1  <- qlogis(se)
  u2  <- qlogis(sp)
  
  t11 <- rep(c(rep(0.5, 2), rep(1,2), rep(4,2)), 3) ## t1 = 0.5
  t22 <- rep(c(rep(0.5, 2), rep(4,2), rep(1,2)), 3) ## t2 = 0.5
  r   <- rep(c(-0.3, -0.6), 9)
  t12 <- sqrt(t11*t22)*r
  
  b   <- rep(0.5, 18)
  a   <- c(-0.442, -0.465, -0.181, -0.255, -0.182, -0.253, -1.007, -1.032, -0.774,
           -0.858, -0.782, -0.852, -0.457, -0.502, -0.211, -0.291, -0.213, -0.288)
  
  c11 <- rep(0.5, 18)
  c22 <- rep(0.5, 18)
  
  sauc<- dtametasa::sAUC(rbind(u1, u2, t22, t12))
  
  set <- cbind(S, u1, u2, t11, t22, t12, c11, c22, b, a, sauc, r, se, sp)
  
  return(round(set,3))
  
})

set <- set.all
save(set, file = "18rows/set-0.5b-all.RData")


